number1 = int(input('first number : '))
number2 = int(input('second number : '))

add = number1 + number2
print(f'{number1} + {number2} = {add}')

sub = number1 - number2
print(f'{number1} - {number2} = {sub}')

mul = number1 * number2
print(f'{number1} X {number2} = {mul}')

div = number1 / number2
print(f'{number1} / {number2} = {div}')